"""
Comparison and logical expression print examples.
"""

def run():
    print("\n-- Relational / Comparison Expressions --")
    print("5 > 3  ->", 5 > 3) 
    print("5 < 3  ->", 5 < 3)
    print("5 == 5 ->", 5 == 5)
    print("5 != 4 ->", 5 != 4)
    print("5 >= 5 ->", 5 >= 5)
    print("4 <= 6 ->", 4 <= 6)

    print("\n-- Logical Expressions --")
    print("True and False ->", True and False)
    print("True or False  ->", True or False)
    print("not True       ->", not True)

if __name__ == "__main__":
    run()
