"""
Arithmetic expression print examples.
"""

def run():
    print("\n-- Arithmetic Expressions --")
    print("2 + 3  =", 2 + 3)
    print("5 - 2  =", 5 - 2)
    print("3 * 4  =", 3 * 4)
    print("10 / 2 =", 10 / 2)     # float
    print("10 // 3 =", 10 // 3)   # floor division
    print("10 % 3 =", 10 % 3)     # modulus
    print("2 ** 3 =", 2 ** 3)     # exponentiation

if __name__ == "__main__":
    run()