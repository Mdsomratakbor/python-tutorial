def run():
    print("\n-- String Casting & Boolean --")
    print("bool(''):", bool(''))      # False
    print("bool(' '):", bool(' '))    # True
    print("bool('Hi'):", bool('Hi'))  # True

    num = 123
    print("str(123):", str(num))      # "123"
    s = "456"
    print("int('456'):", int(s))      # 456
